especificada más arriba.

<P>

<H1><A NAME="SECTION00060000000000000000">
5 Qué es una Topología de red</A>
</H1>

<P>
Por topología de red, se entiende la forma en la cual esta diseñada la red a 
la que pertenecemos. Esto incluye desde la cantidad de máquinas que hay, hasta 
la forma en la cual están interconectadas.

<P>

<H1><A NAME="SECTION00070000000000000000">
6 Preparando un Gateway ADSL</A>
</H1>

<P>

<H2><A NAME="SECTION00071000000000000000">
6.1 Situación ideal y Topología de la red</A>
</H2>

<P>
Idealizando la situación, supongamos que tenemos dos placas de red en el <B>server</B>, el modem ADSL y dos máquinas <B>cliente</B> (o más de estos últimos).
En este caso, necesitaremos para conectar toda nuestra red, un Hub o Switch.

<P>
Si solamente tenemos el <B>server</B>  (con dos placas de red), modem ADSL y una 
sola máquina <B>cliente</B>, no será necesario ni un Hub ni un Switch. 

<P>

<H3><A NAME="SECTION00071100000000000000">
Para el primer caso (dos o más <B>clientes</B>, y un Hub)</A>
</H3>

<P>
El modem ADSL, lo conectamos a una de las placas de red del servidor (digamos
eth1), y la otra placa de red (para ser ordenados, eth0) la conectaremos al
Hub/Switch, de la siguiente manera:

<P>
<TABLE  WIDTH="95%">
<TR><TD>
<PRE>
 Linea Telefónica                                  ========&gt; CLIENTE-1
   ^                                               ||
   |                         -----                 ||
   |                         | L | eth0      ----------------
   |                         | I | &lt;=======&gt; | HUB / SWITCH |=====&gt; CLIENTE-2
   |                         | N |           ----------------
 --------------        eth1  | U |             ||
 | modem ADSL | &lt;==========&gt; | X |             =========&gt; CLIENTE-N 
 --------------              -----
</PRE></TD></TR>
</TABLE>

<P>

<H3><A NAME="SECTION00071200000000000000">
Para el segundo caso (Un solo <B>cliente</B>, y sin Hub)</A>
