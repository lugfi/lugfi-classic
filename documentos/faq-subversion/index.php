<H1><A NAME="SECTION00020000000000000000">
1 Uso básico.</A>
</H1>

<P>

<H2><A NAME="SECTION00021000000000000000">
1.1 Ya tengo una cuenta de svn, ¿y ahora?</A>
</H2>

<P>
Ahora asumo que ya tenés el svn andando también. Si no es el caso
y usas Debian (<TT>unstable</TT>), hacé un:

<P>

<DL COMPACT>
<DT>
<DD>#&nbsp;apt-get&nbsp;install&nbsp;subversion
</DD>
</DL>Listo, ya tenés el subversion. Si usas otra distro, buscá si está
disponible un paquete (seguramente sí) y si no vas a tener que compilarlo
vos. En la página del subversion podés encontrar paquetes precompilados http://subversion.tigris.org/servlets/ProjectDocumentList
para las distros más populares.

<P>

<H3><A NAME="SECTION00021100000000000000">
1.1.1 ¿Y si uso Debian <TT>stable</TT>? En la página web del subversion
solo está el código fuente y binarios RPM ¿qué bajo?</A>
</H3>

<P>
Yo diría que pruebes con el código fuente. O probá de apuntar tu <TT>/etc/apt/sources.list</TT>
al <TT>unstable</TT> y fijate si no te tiene que actualizar media
distribución para bajar el paquete. Es un paquete que (creo que) no
tiene muchas dependencias, así que con un poco de suerte...

<P>

<H2><A NAME="SECTION00022000000000000000">
1.2 Ya tengo mi cuenta de svn y mi <TT>svn</TT> andando ¿y ahora?</A>
</H2>

<P>
Ahora podés hacer un <I>checkout</I> del repositorio, solamente escribí
esto:

<P>

<DL COMPACT>
<DT>
<DD>$&nbsp;svn&nbsp;co&nbsp;http://svn.llucax.hn.org/svn/lugfi/tronco&nbsp;lugfi-www
</DD>
</DL>(Reemplaza el <TT>http://svn.llucax.hn.org/svn/lugfi/tronco</TT> por
el repositorio que quieras bajar y el <TT>lugfi</TT> del final por
el directorio en donde querés que baje todo)

<P>
Esto te va a crear un subdirectorio <TT>lugfi-www</TT> después de
preguntarte tu usuario y password. En ese subdirectorio está todos
los archivos de sitio.

<P>

<H2><A NAME="SECTION00023000000000000000">
1.3 ¿Cómo modifico un archivo?</A>
</H2>

<P>
Entrá al subdirectorio lugfi-www y editalo con tu editor favorito!
Hacé de cuenta que estás trabajando en un subdirectorio cualquiera.
Modificá todos los archivos que quieras, el repositorio no se va a
modificar hasta que hagas un <I>commit</I>.

<P>

<H2><A NAME="SECTION00024000000000000000">
1.4 ¿Qué es un <TT>commit</TT>?</A>
</H2>

<P>
Con el comando <TT>commit</TT> del <TT>svn</TT> se <I>suben</I> los
cambios al repositorio. Es recomendado hacer un <TT>update</TT> antes:

<P>

<DL COMPACT>
<DT>
<DD>.../lugfi-www/$&nbsp;svn&nbsp;up

<P>
[...]

<P>
At&nbsp;revision&nbsp;30.

<P>
.../lugfi-www/$&nbsp;svn&nbsp;ci&nbsp;[archivos]

<P>
A&nbsp;archivo

<P>
M&nbsp;archivo

<P>
[...]
</DD>
</DL>El <TT>svn ci</TT> (commit) te va a abrir tu editor favorito (o se
va a quejar si no puede hacerlo) para que escribas un mensaje comentando
qué es lo que cambiaste. !Por favor, tratá de ser lo más descriptivo
posible!

<P>

<H2><A NAME="SECTION00025000000000000000">
1.5 ¿Qué es un <TT>update</TT>?</A>
</H2>

<P>
El comando <TT>update</TT> se encarga de poner al día nuestra copia
local del repositorio. Si alguien hizo algún cambio después de que
lo hayamos bajado, este comando nos lo actualiza de una forma <I>barata</I>,
ya que sólo baja los cambios (no todo el repositorio de nuevo).

<P>

<DL COMPACT>
<DT>
<DD>$&nbsp;svn&nbsp;up&nbsp;[&lt;archivo&gt;]
</DD>
</DL><TT>&lt;archivo&gt;</TT> es opcional (como en prácticamente todos los comandos
de <TT>svn</TT>), si no se especifica, se toma el directorio actual.

<P>

<H2><A NAME="SECTION00026000000000000000">
1.6 ¿Cómo sé que cosas cambiaron/van a cambiar antes de hacer un <TT>commit</TT>/<TT>update</TT>?</A>
</H2>

<P>
Para tener un pantallazo, el comando <TT>status</TT> es tu mejor amigo:

<P>

<DL COMPACT>
<DT>
<DD>$&nbsp;svn&nbsp;st
</DD>
</DL>Si querés ver qué cosa cambió en cada archivo, el comando <TT>diff</TT>
es lo que necesitas:

<P>

<DL COMPACT>
<DT>
<DD>$&nbsp;svn&nbsp;diff&nbsp;&lt;archivo&gt;
</DD>
</DL>
<P>

<H2><A NAME="SECTION00027000000000000000">
1.7 Me dice algo como que el repositorio está lockeado, ¿Qué pasa?</A>
</H2>

<P>
Es probable que hayas cortado un comando del <TT>svn</TT> con <TT>Ctrl-C</TT>
o algo así. Probá de hacer un:

<P>

<DL COMPACT>
<DT>
<DD>$&nbsp;svn&nbsp;cleanup
</DD>
</DL>y si eso no lo soluciona preguntá en lugfi-www.

<P>

<H2><A NAME="SECTION00028000000000000000">
1.8 ¿Cómo agrego/muevo/borro un archivo/directorio?</A>
</H2>

<P>
Para agregar archivos o directorios (de forma recursiva por defecto)
está el comando add:

<P>

<DL COMPACT>
<DT>
<DD>$&nbsp;svn&nbsp;add&nbsp;miarchivo&nbsp;midirectorio

<P>
A&nbsp;miarchivo

<P>
A&nbsp;midirectorio
</DD>
</DL>Para mover (renombrar) un archivo o directorio es casi igual que lo
que hacés frecuentemente, sólo que hay que agregar antes el svn:

<P>

<DL COMPACT>
<DT>
<DD>$&nbsp;svn&nbsp;mv&nbsp;miarchivo&nbsp;milindoarchivo

<P>
D&nbsp;miarchivo

<P>
A+&nbsp;milindoarchivo

<P>
$&nbsp;svn&nbsp;rm&nbsp;otroarchivo

<P>
D&nbsp;otroarchivo
</DD>
</DL>Todos estos cambios (y cualquier cambio que hagas) no se subirá al
repositorio hasta que no hagas un <TT>commit</TT>.

<P>

<H2><A NAME="SECTION00029000000000000000">
1.9 ¡Ups! Rompí algo, ¿cómo hago para volver atrás?</A>
</H2>

<P>
Si todavía no hiciste un commit, es fácil, simplemente escribí:

<P>

<DL COMPACT>
<DT>
<DD>$&nbsp;svn&nbsp;revert&nbsp;[&lt;archivo&gt;]
</DD>
</DL>Si ya hiciste el <I>commit</I>, tendrías que hacer un <TT>merge</TT>
con la versión anterior pero eso escapa a los objetivos de este documento.

<P>

<H2><A NAME="SECTION000210000000000000000">
1.10 ¿Qué son esas cosas raras <TT>$id$</TT>, <TT>$date$</TT>, etc?</A>
</H2>

<P>
Son <I>keywords</I>, es decir, palabras clave que el <TT>svn</TT>
puede traducir a propiedades del documento. En realidad van con mayúscula
la primera letra pero lo puse a propósito mal para que en el título
no lo expanda. A continuación las escribo bien así son expandidas
y ven el significado de cada una:

<P>

<DL COMPACT>
<DT>
<DD>$Id:&nbsp;faq-subversion.lyx&nbsp;37&nbsp;2003-08-06&nbsp;02:37:18Z&nbsp;llucare&nbsp;$

<P>
$Date:&nbsp;2003-08-05&nbsp;23:37:18&nbsp;-0300&nbsp;(Tue,&nbsp;05&nbsp;Aug&nbsp;2003)&nbsp;$

<P>
$Rev:&nbsp;37&nbsp;$

<P>
$URL:&nbsp;http://svn.llucax.hn.org:81/svn/lugfi/tronco/proyectos/faq_website.lyx&nbsp;$

<P>
$Author:&nbsp;llucare&nbsp;$
</DD>
</DL>
<P>

<H2><A NAME="SECTION000211000000000000000">
1.11 ¿Por qué a mí no me expande los <I>keywords</I>?</A>
</H2>

<P>
El <TT>svn</TT> por defecto no expande nada para evitar problemas
indeseables, tengan en cuenta que en especial al trabajar con PHP
(o Perl) se usa mucho el signo $ y si usamos una variable <TT>$Id</TT>
y luego otra es probable que lo expanda. Es por esto que recomiendo
altamente <B>no</B> utilizar nombres de variable en PHP que empiecen
con Mayúsculas (para evitar problemas con futuros <I>keywords</I>
también). En fin, en caso de querer que expanda los <I>keywords</I>
hay que decirlo explícitamente, seteando la propiedad especial (los
archivos además del contenido pueden tener propiedades arbitrarias)
llamada <TT>svn:keywords</TT>:

<P>

<DL COMPACT>
<DT>
<DD>$&nbsp;svn&nbsp;ps&nbsp;svn:keywords&nbsp;``Id&nbsp;Date&nbsp;Author''&nbsp;&lt;archivo&gt;
</DD>
</DL>Donde <TT>``Id Date Author''</TT> puede ser en realidad cualquier
combinación de <I>keywords</I> que queramos expandir. También podemos
hacer un:

<P>

<DL COMPACT>
<DT>
<DD>$&nbsp;svn&nbsp;pe&nbsp;svn:keywords&nbsp;&lt;archivo&gt;
</DD>
</DL>Para que abra nuestro editor favorito y escribir en él los <I>keywords</I>
a expandir.

<P>
<B>Es importante que no expandan keywords en archivos binarios,
porque pueden tener la mala suerte de que les parta el archivo al
medio al expandir algo no deseado.</B>

<P>

<H2><A NAME="SECTION000212000000000000000">
1.12 ¿Hay una forma más fácil de poner los <I>keywords</I>?</A>
</H2>

<P>
No que conozca, pero para simplificar un poco las cosas (y considerando
que siempre expando los mismos <I>keywords</I>) me hice dos <I>micro-scripts</I>:

<P>

<DL COMPACT>
<DT>
<DD><B>svnkw:</B>

<P>
#!/bin/sh

<P>
svn&nbsp;ps&nbsp;svn:keywords&nbsp;``Id&nbsp;Date&nbsp;Author&nbsp;Rev&nbsp;URL''&nbsp;``$@''
</DD>
</DL>que setea los <I>keywords</I> y

<P>

<DL COMPACT>
<DT>
<DD><B>svnaddkw:</B>

<P>
#!/bin/sh

<P>
svn&nbsp;add&nbsp;``$@''

<P>
svn&nbsp;ps&nbsp;svn:keywords&nbsp;``Id&nbsp;Date&nbsp;Author&nbsp;Rev&nbsp;URL''&nbsp;``$@''
</DD>
</DL>que agrega los archivos y setea los <I>keywords</I>.
