<span class="LugFI-Span_Titulo">:: CDoteca ::</span>
<br>
<!-- FIN $Id: default-titulo.php,v 1.2 2004/04/28 04:09:24 emiliano Exp $ -->

<p>

La idea del proyecto es armar una colección de CDs de software libre accesible
a cualquiera que pueda llegar a necesitar alguno de ellos. Está funcionando en
el LABI, ubicado en la sede Paseo Colón de la Facultad, primer piso (enfrente
del Departamento de Electrónica). No es posible tomar prestados los CDs, pero
sí hacer copias. Echá un vistazo a la lista de CDs actualmente disponibles.

<p>

<!-- INICIO $Id: default-titulo.php,v 1.2 2004/04/28 04:09:24 emiliano Exp $ -->
<span class="LugFI-Span_Titulo">:: Modus Operandi ::</span>
<br>
<!-- FIN $Id: default-titulo.php,v 1.2 2004/04/28 04:09:24 emiliano Exp $ -->

<p>

Para grabarte un CD, el procedimiento es más o menos el siguiente:

<ol>
    <li>Ir de 10 a 22 a la biblioteca del LABI.</li>
    <li>Entregar a la persona a cargo del LABI un CD virgen más $2 por cada
        CD que quieras copiar.</li>
    <li>Volver un par de días después a buscar tu(s) CD(s) copiado(s) en
        el mismo horario (para estar seguro, preguntale a la persona a cargo
        cuando pasar).</li>
</ol>

Por favor, sean tolerantes con la gente del LABI, nos están haciendo un favor
(a nosotros y a ustedes).

<p>
 
