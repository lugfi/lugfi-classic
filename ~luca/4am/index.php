                <h4 class="serendipity_title"><a href="/~luca/4am/archives/12-Worse-is-better.html">Worse is better</a></h4>

        <div class="serendipity_entry serendipity_entry_author_Leandro-Lucarella ">
                        <span class="serendipity_entryIcon">
                                                <a href="http://www.lug.fi.uba.ar/~luca/4am/categories/5-Teoria-de-Lenguaje"><img class="serendipity_entryIcon" title="Teoría de Lenguaje: El lenguaje modela el pensamiento, sin un buen lenguaje no nos podemos expresar" alt="Teoría de Lenguaje" src="/~luca/4am/uploads/categorias/lenguajes.png" /></a>
                                                                <a href="http://www.lug.fi.uba.ar/~luca/4am/categories/12-Sistema-Operativo"><img class="serendipity_entryIcon" title="Sistema Operativo: The puppet master" alt="Sistema Operativo" src="/~luca/4am/uploads/categorias/os.png" /></a>
                                        </span>
            
            <div class="serendipity_entry_body">
                <p>Por casualidad me encontré con un pequeño documento que da una interesante explicación al éxito (o supervivencia?) de Unix y C. El documento se llama <a target="_self" href="http://www.jwz.org/doc/worse-is-better.html">The Rise of "Worse is Better"</a> y dice que Unix y C fueron diseñador con la simplicidad (y en particular de la implementación) por sobre todos los demás aspectos (incluso la <i>correctitud</i>, <i>completitud</i> y simplicidad de las interfaces), a diferencia de otros pares, dando como ejemplo a LISP de lenguaje <i>hecho bien</i> que no prosperó.</p>

<p>Creo que esto podría ser aplicable a Linux vs. HURD, si bien teóricamente un microkernel pueda ser <strong>mejor</strong>, un kernel monolítico es más <strong>simple</strong> y tiene muchas más chances de sobrevivir y de adaptarse a otras plataformas (ser más portable) y por lo tanto expandirse, aún cuando el diseño pueda ser teóricamente <i>peor</i> que el de HURD (y no sé si realmente sea el caso, pero es de los pocos argumentos que se escuchan a favor de los microkernels).</p>

<p>En fin, sea como sea me parece interesante la lectura del artículo ;)</p>


