                <h4 class="serendipity_title"><a href="/~luca/4am/archives/9-VL.html">VL</a></h4>

        <div class="serendipity_entry serendipity_entry_author_Leandro-Lucarella ">
                        <span class="serendipity_entryIcon">
                                                <a href="http://www.lug.fi.uba.ar/~luca/4am/categories/6-Reference-Counting"><img class="serendipity_entryIcon" title="Reference Counting: 2 + 2 = 5" alt="Reference Counting" src="/~luca/4am/uploads/categorias/counter.png" /></a>
                                        </span>
            
            <div class="serendipity_entry_body">
                <p>Volvemos al <a target="_self" href="http://www.lug.fi.uba.ar/~luca/4am/archives/4-La-aventura-de-contar-referencias.html">conteo de referencias</a>, ahora con el algoritmo que hemos llamado <strong>VL</strong>. El nombre surge de un flag al cual no sabía que nombre ponerle, y a falta de mejor nombre, al hablar con Alberto, le decía <i>valor_loco</i> (<i>VL</i> para ahorrar palabras).</p>

<p>Bien la idea del algoritmo es detectar <i>ciclos simples</i> sin referencias externas y liberarlos en una sola pasada, utilizando un flag (<strong>VL</strong>) que actuaría de algo así como <i>dirty bit</i>. La idea es comenzar con <tt>VL = 0</tt> y mantenerlo hasta encontrar, en la recorrida de la primera pasada del <a target="_self" href="http://www.lug.fi.uba.ar/~luca/4am/archives/7-L+.html">L+</a>, algún nodo con <tt>RC &gt; 0</tt>. Además hay que guardar el nodo del cual partimos (<strong>R</strong>). Entonces, si en algún momento llegamos al principio, tenemos su contador en cero y el flag es cero (<tt>nodo_actual == R and RC(R) == 0 and VL == 0</tt>), podemos asegurar que encontramos un ciclo simple, y podemos liberar, al retornar en el stack, todos los nodos por los que pasamos.</p>


            </div>

            
